""" 카운터에는 500, 100, 50, 10원 짜리 동전이 존재
거슬러 줘야 할 돈이 N원일 때 거슬러 줘야 할 동전의 최소 개수?
단, 거슬러 줘야 할 돈 N은 항상 10의 배수 """

money = 1260
change = 0
coins = [500, 100, 50, 10]
for i in coins:
    change += money//i
    money %=i
print(change)

""" 시간 복잡도는 O(k)
이 알고리즘의 시간 복잡도는 동전의 총 종류에만 영향을 받고
거슬러 줘야 하는 금액의 크기와는 무관 """